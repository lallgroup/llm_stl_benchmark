def standardize_parameters(MODEL, TEMP, MAX_OUTPUT_TOKENS, kwargs)->SamplingParams:
  # use recommended parameters for Qwen3.5
  if MODEL == "Qwen/Qwen3.5-9B":
    #Thinking mode for precise coding tasks (e.g. WebDev):
    TEMP=0.6
    kwargs = {"top_p":0.95,
              "top_k":20,
              "min_p":0.0,
              "presence_penalty":0.0,
              "repetition_penalty":1.0
    }
  if MODEL == "Qwen/Qwen3-Coder-30B-A3B-Instruct":
    TEMP=0.7
    kwargs = {"top_p":0.8,
              "top_k":20,
              "min_p":0.0,
              "repetition_penalty":1.05
    }
    MAX_OUTPUT_TOKENS = 65536

  MAX_OUTPUT_TOKENS = max(MAX_OUTPUT_TOKENS, 512)
  
  return SamplingParams(temperature=TEMP, max_tokens=MAX_OUTPUT_TOKENS, **kwargs)

def load_model(MODEL, params, tensor_parallel_size=4)->LLM:
  return LLM(model=MODEL, dtype="bfloat16", tensor_parallel_size=tensor_parallel_size)

class ChatModel:
  def __init__(self, MODEL, params, tensor_parallel_size=4):
    self.model = load_model(MODEL, params, tensor_parallel_size=tensor_parallel_size)
    self.params = params

  def chat(self, prompt:str)->str:
    response = self.model.generate([prompt], self.params)
    return response[0].outputs[0].text

  def chat_with_tokens(self, prompt:str)->tuple[str, int, int]:
    """Returns (text, input_tokens, output_tokens)."""
    response = self.model.generate([prompt], self.params)
    output = response[0]
    text = output.outputs[0].text
    input_tokens = len(output.prompt_token_ids)
    output_tokens = len(output.outputs[0].token_ids)
    return text, input_tokens, output_tokens


def log(msg:str):
  print(f"[{time.strftime('%H:%M:%S')}] {msg}", flush=True)
  sys.stdout.flush()

def log_gpu_memory(label=""):
  if not torch.cuda.is_available():
    log("CUDA not available")
    return
  for i in range(torch.cuda.device_count()):
    alloc = torch.cuda.memory_allocated(i) / 1024**3
    reserved = torch.cuda.memory_reserved(i) / 1024**3
    total = torch.cuda.get_device_properties(i).total_memory / 1024**3
    log(f"  GPU {i} {label}: allocated={alloc:.2f}GB  reserved={reserved:.2f}GB  total={total:.2f}GB")


def get_first_valid(response:str)->Optional[str]:
  response = response.strip()
  if '```' in response:
    s = response.split('```')
    for i in range(len(s)):
      if len(s[i].strip()) == 0:
        continue
      if s[i].startswith("python"):
        s[i] = s[i][6:].strip() # clip this off
      try:
        byte_code = compile(s[i], "<user_code>", "exec")
        print(s[i], flush=True)
        return s[i]
      except Exception as e:
        print(e, flush=True)
  return None


